# count - its shows the frequency of element 
l=[10,23,32,23,23,4,4,2,2,5]
w=["Hello","Shubham"]
a=l.count(23)
print(a)

#max - shows the max value element in alpha it works on alphabetical order 
b = max(l)
print(b)
d = max(w)
print(d)

# min 
c = min(l)
print(c)
e = min(w)
print(e)

